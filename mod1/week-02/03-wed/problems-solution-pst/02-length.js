/*

Write a function called getLengths that will take an array and return the size of that array


*/


function getLengths(arr){
    return arr.length;
}






let arr1 = ["a", "b", "c", "d"];
let arr2 = ["anthony", "sam", "edward", "gabe"];
let arr3 = [1, 2, 3];
console.log(getLengths(arr1)); // 4
console.log(getLengths(arr2));  // 4
console.log(getLengths(arr3));  // 3
