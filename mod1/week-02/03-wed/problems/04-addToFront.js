/*

Write a function called addToFront that will take an array and an element and add the element to the front of the array.
The function should return the modified array

*/






let arr1 = ["a", "b", "c", "d"];
let arr2 = ["anthony", "sam", "edward", "gabe"];
let arr3 = [1, 2, 3];

console.log(addToFront(arr1, "e")); // ["e", "a", "b", "c", "d"];
console.log(addToFront(arr2, "alexi")); // ["alexi", "anthony", "sam", "edward", "gabe"];
console.log(addToFront(arr3, 4)); // [4, 1, 2, 3];
